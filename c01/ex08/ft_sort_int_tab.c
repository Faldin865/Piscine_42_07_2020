void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int temp;

	i = 0;
	temp = 0;
	while (size > i)
	{
		if (tab[i - 1] > tab[i] && i != 0)
		{
			temp = tab[i];
			tab[i] = tab[i - 1];
			tab[i - 1] = temp;
			i = 0;
		}
		i++;
	}
}
