#include <unistd.h>

int		main(int argc, char **argv)
{
	int i;
	int n;

	n = 1;
	i = 0;
	while (n < argc)
	{
		while (argv[n][i])
		{
			write(1, &argv[n][i], 1);
			i++;
		}
		write(1, "\n", 1);
		i = 0;
		n++;
	}
	return (0);
}
