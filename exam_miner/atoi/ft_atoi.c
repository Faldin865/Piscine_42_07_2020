int		ft_atoi(char *str)
{
	int		i;
	int		signe;
	int		re;

	signe = 1;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		re = re * 10 + (str[i] - '0');
		i++;
	}
	if (signe == -1)
		return (-re);
	else
		return (re);
}
