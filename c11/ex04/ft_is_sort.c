int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int		i;
	int		c;
	int		d;

	i = 1;
	c = 1;
	d = 1;
	while (i < length)
	{
		if ((*f)(tab[i - 1], tab[i]) > 0)
			c = 0;
		if ((*f)(tab[i - 1], tab[i]) < 0)
			d = 0;
		i++;
	}
	if (c || d)
		return (1);
	else
		return (0);
}
