int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int		i;
	int		n;

	n = 0;
	i = 0;
	while (i < length)
	{
		if ((*f)(tab[i]) != 0)
			n++;
		i++;
	}
	return (n);
}
